#include "incs/cub3d.h"

void	skip_spaces(char *str, int *i)
{
	while (*(str + (*i)) != '\0' && *(str + (*i)) == ' ')
		(*i)++;
}

void	skip_str(char *str, int *i)
{
	while (*(str + (*i)) != '\0' && *(str + (*i)) != ' ' && *(str + (*i)) != '\n')
		(*i)++;
}